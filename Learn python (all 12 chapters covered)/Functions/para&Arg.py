# Parameters & Arguments
# Parameters is a variable that acts as a placeholder for a value that is passed into a function
# Arguments is a value that is passed into a function

def greet(name): #name - parameter
    '''displaying a hi message to user''' #docstring
    print("Hi", name, ", how are you?")

greet("Sujay") #Sujay - argument
greet("Suman") #Sujay - argument
greet("Rahul") #Sujay - argument

